package com.w3code.library.json;

public class Base {

    public String mName;
    public String mDetails;
    public String mLogo;
    public String mBanner;
    public String mStart;
    public String mEnd;

    public String getBaseName() {return mName; }
    public void setBaseName(String name) { this.mName = name; }
    public String getBaseDetails() {return mDetails; }
    public void setBaseDetails(String details) { this.mDetails = details; }
    public String getBaseLogo() {return mLogo; }
    public void setBaseLogo(String logo) { this.mLogo = logo; }
    public String getBaseBanner() {return mBanner; }
    public void setBaseBanner(String banner) { this.mBanner = banner; }
    public String getBaseStart() {return mStart; }
    public void setBaseStart(String start) { this.mStart = start; }
    public String getBaseEnd() {return mEnd; }
    public void setBaseEnd(String end) { this.mEnd = end; }
}